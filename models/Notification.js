const mongoose = require("mongoose");
 
const NotificationSchema = new mongoose.Schema({
    userId: {
        type: mongoose.Schema.Types.ObjectId,
        ref: "User",
        required: true
    },
    type: {
        type: String,
        enum: ["inactive_reminder", "new_review", "trip_saved"],
        required: true
    },
    message: {
        type: String,
        required: true
    },
    read: {
        type: Boolean,
        default: false
    }
}, { timestamps: true });
 
module.exports = mongoose.model("Notification", NotificationSchema);